public class Operator {
    public static void main(String[] args) {

        int x=5;
        int y=25;

        int z = x+y ;
//        + -> operator
//      x,y -> operands

//        increment and decrement operator

        int m = 5 ;
        int n = m++ ;
        System.out.println("m is "+m+", n is "+n);

        int p = 5 ;
        int q = ++p ;
        System.out.println("p is "+p+ ", q is "+q);
    }
}
